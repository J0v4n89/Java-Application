    private void jButton_izmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_izmeniActionPerformed
           String naziv = jTextFieldNaziv.getText();
           String isbn = jTextFieldISBN.getText();
           int godIzdanja;

           try {
               godIzdanja = Integer.parseInt(jTextFieldGodIzdanja.getText());
           } catch (NumberFormatException e) {
               JOptionPane.showMessageDialog(this, "Godina izdanja mora biti broj!", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
               return;
           }
           Zanr zanr = (Zanr)jComboBox_Zanr.getSelectedItem();
           Autor odabraniAutor = (Autor) jComboBoxAutori.getSelectedItem();
           
            knjigaZaIzmenu.setAutor(odabraniAutor);
            knjigaZaIzmenu.setNaslov(naziv);
            knjigaZaIzmenu.setGodinaIzdanja(godIzdanja);
            knjigaZaIzmenu.setZanr(zanr);

           gf.osveziTabelu();
           JOptionPane.showMessageDialog(this, "Knjiga je uspesno dodata", "Uspeh", JOptionPane.INFORMATION_MESSAGE);
           this.dispose();
    }//GEN-LAST:event_jButton_izmeniActionPerformed

    private void jButton_dodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_dodajActionPerformed
        int godIzdanja;
        String naziv = jTextFieldNaziv.getText();
        String isbn = jTextFieldISBN.getText();
        try{
        godIzdanja = Integer.parseInt(jTextFieldGodIzdanja.getText());
        
     } catch (NumberFormatException e){
         JOptionPane.showMessageDialog(this, "Niste uneli ispravno podatke", "Greska", JOptionPane.WARNING_MESSAGE);
        return;
    }//GEN-LAST:event_jButton_dodajActionPerformed
    Zanr zanr = (Zanr) jComboBox_Zanr.getSelectedItem();
    Autor odabraniAutor = (Autor) jComboBoxAutori.getSelectedItem();
    Knjiga knjiga = new Knjiga(naziv, odabraniAutor, isbn, godIzdanja, zanr);
    controller.dodajKnjigu(knjiga);
    gf.osveziTabelu();
    JOptionPane.showMessageDialog(this, "Knjiga je uspesno dodata", "Uspeh", JOptionPane.INFORMATION_MESSAGE);
    this.dispose();
}
